create table markets
(
    id              bigint generated by default as identity primary key,
    created_at      timestamp with time zone,
    updated_at      timestamp with time zone,
    deleted_at      timestamp with time zone,
    pricing_decimal numeric,
    trading_decimal numeric,
    taker_fee_rate  numeric,
    maker_fee_rate  numeric,
    destination_id  bigint  constraint fk_markets_destination references assets,
    issue_date      timestamp with time zone,
    min_amount      numeric,
    source_id       bigint constraint fk_markets_source references assets,
    is_amm          boolean,
    name            text unique,
    status          text
);

alter table markets owner to postgres;

create index idx_markets_deleted_at
    on markets (deleted_at);


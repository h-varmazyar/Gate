create table indicators
(
    id                                bigint generated by default as identity primary key,
    created_at                        timestamp with time zone,
    updated_at                        timestamp with time zone,
    deleted_at                        timestamp with time zone,
    type                              varchar(64),
    market_id                         bigint,
    resolution_id                     bigint,
    configs_rsi_period                bigint,
    configs_rsi_source                varchar(16),
    configs_stochastic_period         bigint,
    configs_stochastic_smooth_k       bigint,
    configs_stochastic_smooth_d       bigint,
    configs_sma_period                bigint,
    configs_sma_source                varchar(16),
    configs_bollinger_bands_period    bigint,
    configs_bollinger_bands_deviation bigint,
    configs_bollinger_bands_source    varchar(16)
);

alter table indicators
    owner to postgres;

create index idx_assets_deleted_at on indicators (deleted_at);
